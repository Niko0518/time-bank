// 应用屏幕时间修正
async function applyScreenTimeCorrection(txId, origDate) {
    const hours = parseInt(document.getElementById('correctHours').value) || 0;
    const mins = parseInt(document.getElementById('correctMins').value) || 0;
    const newUsedMins = hours * 60 + mins;
    
    if (newUsedMins < 0 || newUsedMins > 1440) {
        showAlert('使用时间应在 0-1440 分钟之间', '输入错误');
        return;
    }
    
    // 找到交易记录
    const tx = transactions.find(t => t.id === txId);
    if (!tx) {
        showAlert('未找到该记录，可能已被删除', '错误');
        hideCorrectScreenTimeModal();
        return;
    }
    
    const oldUsedMins = tx.screenTimeData?.usedMinutes || 0;
    const limitMins = tx.screenTimeData?.limitMinutes || screenTimeSettings.dailyLimit;
    const oldType = tx.type;
    const oldAmount = tx.amount;
    
    // 计算新的 amount
    // 逻辑: (限额 - 使用) * 60 = 秒数
    const newDiffMins = limitMins - newUsedMins;
    const newAmount = Math.abs(newDiffMins) * 60;
    const newType = newDiffMins >= 0 ? 'earn' : 'spend';
    
    // 计算余额调整
    const oldEffect = oldType === 'earn' ? oldAmount : -oldAmount;
    const newEffect = newType === 'earn' ? newAmount : -newAmount;
    const balanceChange = newEffect - oldEffect;
    
    // 确认
    const confirmMsg = `确认修正 ${origDate} 的屏幕时间？\n\n` +
        `使用时间: ${Math.floor(oldUsedMins/60)}时${oldUsedMins%60}分 → ${hours}时${mins}分\n` +
        `每日限额: ${Math.floor(limitMins/60)}时${limitMins%60}分\n` +
        `交易类型: ${oldType === 'earn' ? '赚取' : '消费'} → ${newType === 'earn' ? '赚取' : '消费'}\n` +
        `时间金额: ${formatTime(oldAmount)} → ${formatTime(newAmount)}\n` +
        `余额变化: ${balanceChange >= 0 ? '+' : ''}${formatTime(balanceChange)}`;
    
    if (!await showConfirm(confirmMsg, '确认修正')) {
        return;
    }
    
    // 更新交易记录
    tx.screenTimeData.usedMinutes = newUsedMins;
    tx.type = newType;
    tx.amount = newAmount;
    tx.note = `手动修正于 ${new Date().toLocaleString()}`;
    
    // 调整余额
    currentBalance += balanceChange;
    
    // 更新 screenTimeHistory
    let history = JSON.parse(localStorage.getItem('screenTimeHistory') || '[]');
    const historyIdx = history.findIndex(h => h.date === origDate || h.actualDate === origDate);
    if (historyIdx >= 0) {
        history[historyIdx].usedMinutes = newUsedMins;
        history[historyIdx].earnedSeconds = newType === 'earn' ? newAmount : 0;
        history[historyIdx].type = newType;
        history[historyIdx].amount = newAmount;
        localStorage.setItem('screenTimeHistory', JSON.stringify(history));
    }
    
    // 同步到云端
    if (isLoggedIn()) {
        try {
            await DAL.updateTransaction(tx);
            console.log(`[CorrectScreenTime] 已同步到云端: ${txId}`);
        } catch (e) {
            console.error('[CorrectScreenTime] 云端同步失败:', e);
        }
    }
    
    // 保存数据
    saveData();
    updateBalanceDisplay();
    updateAllUI();
    
    hideCorrectScreenTimeModal();
    showAlert(`已修正 ${origDate} 的屏幕时间记录。\n余额变化: ${balanceChange >= 0 ? '+' : ''}${formatTime(balanceChange)}`, '修正完成');
}

