// [v7.25.4] 更新设备 ID 显示
function updateDeviceIdDisplay() {
    const deviceIdDisplay = document.getElementById('deviceIdDisplay');
    if (deviceIdDisplay) {
        const deviceId = clientId || localStorage.getItem('tb_client_id') || 'unknown';
        deviceIdDisplay.textContent = deviceId;
    }
}

// [v7.2.4] 点击✏️展开编辑框
function toggleDeviceNameEdit(event) {
    if (event) event.stopPropagation();
    
    const deviceNameEditRow = document.getElementById('deviceNameEditRow');
    const deviceNameInput = document.getElementById('deviceNameInput');
    const isHidden = deviceNameEditRow.style.display === 'none';
    
    if (isHidden) {
        // 展开编辑框
        deviceNameEditRow.style.display = 'block';
        if (deviceNameInput) {
            deviceNameInput.value = localStorage.getItem('tb_device_name') || '';
            setTimeout(() => {
                deviceNameInput.focus();
                deviceNameInput.select();
            }, 50);
        }
    } else {
        // 收起编辑框
        deviceNameEditRow.style.display = 'none';
    }
}

// [v7.2.4] 确认设备名称
async function confirmDeviceName() {
    const deviceNameInput = document.getElementById('deviceNameInput');
    const newName = deviceNameInput?.value.trim() || '';
    const oldName = localStorage.getItem('tb_device_name') || '';
    
    if (newName && newName !== oldName) {
        const success = await setDeviceName(newName);
        if (success) {
            showToast('✅ 设备名称已更新');
        }
    }
    
    updateDeviceNameDisplay();
}

// [v7.2.4] 回车确认设备名称，Escape取消
function handleDeviceNameKeydown(event) {
    if (event.key === 'Enter') {
        event.preventDefault();
        confirmDeviceName();
    } else if (event.key === 'Escape') {
        event.preventDefault();
        updateDeviceNameDisplay();
    }
}

// [v7.25.4] 复制设备 ID 到剪贴板
function copyDeviceId() {
    const deviceId = clientId || localStorage.getItem('tb_client_id') || 'unknown';
    navigator.clipboard.writeText(deviceId).then(() => {
        showToast('✅ 设备 ID 已复制到剪贴板');
    }).catch(err => {
        console.error('复制失败:', err);
        // 降级方案
        const textArea = document.createElement('textarea');
        textArea.value = deviceId;
        document.body.appendChild(textArea);
        textArea.select();
        try {
            document.execCommand('copy');
            showToast('✅ 设备 ID 已复制到剪贴板');
        } catch (e) {
            showToast('❌ 复制失败，请手动选择复制');
        }
        document.body.removeChild(textArea);
    });
}

// [v7.33.3] 以本设备数据为准：原子化全量同步，防止多设备竞态污染
async function trustThisDeviceAsAuthoritative() {
    if (!isLoggedIn()) {
        showNotification('⚠️ 未登录', '请先登录账号', 'warning');
        return;
    }
    // [v7.26.2] 使用应用内 showConfirm 替代 window.confirm（WebView 中 confirm() 无效）
    if (!await showConfirm(
        '将以本设备数据覆盖云端，期间其他设备无法写入。\n\n确定继续？',
        '🛡️ 以本设备数据为准'
    )) {
        return;
    }

    const authStatus = document.getElementById('authStatus');
    const originalText = authStatus ? authStatus.innerHTML : '';
    if (authStatus) { authStatus.innerHTML = '<span>🛡️ 同步中...</span>'; authStatus.className = 'status-syncing'; }

    try {
        // 1. 停止所有 Watch，防止其他设备在此期间推送旧数据
        if (DAL && DAL.unsubscribeAll) await DAL.unsubscribeAll();

        // 2. 激活写入门禁，阻止 saveData() 等写入云端
        if (typeof activateCloudSyncWriteLock === 'function') {
            activateCloudSyncWriteLock('trust-this-device');
        }

        // 3. 清空云端旧数据
        showNotification('🔄 清空云端旧数据...', '请稍候', 'info');
        await DAL.clearAllData();

        const currentUid = await DAL.getCurrentUid();
        if (!currentUid) throw new Error('无法获取用户 ID');

        const BATCH = 10;
        const DELAY = 150;
        const MAX_RETRY = 3;

        const writeWithRetry = async (col, data, retry = 0) => {
            try {
                await db.collection(col).add(data);
            } catch (err) {
                if (retry < MAX_RETRY) {
                    await new Promise(r => setTimeout(r, 400 * (retry + 1)));
                    return writeWithRetry(col, data, retry + 1);
                }
                console.error(`[trustThisDevice] write failed (${col}):`, err.message);
            }
        };

        // 4. 上传 Profile
        showNotification('📝 上传配置...', '请稍候', 'info');
        const sleepSettingsToUpload = { ...sleepSettings };
        delete sleepSettingsToUpload.settledDates;
        await db.collection(TABLES.PROFILE).add({
            multiTableVersion: MULTI_TABLE_VERSION,
            settings: notificationSettings || {},
            reportState: reportState || {},
            balanceMode: balanceMode,
            sleepSettingsShared: sleepSettingsToUpload,
            categoryColors: [...categoryColors],
            collapsedCategories: [...collapsedCategories],
            deletedTaskCategoryMap: deletedTaskCategoryMap || {},
            _openid: currentUid
        });

        // 5. 上传任务（受控并发）
        showNotification('📋 上传任务...', '0/' + tasks.length, 'info');
        for (let i = 0; i < tasks.length; i += BATCH) {
            const slice = tasks.slice(i, i + BATCH);
            await Promise.all(slice.map(task => writeWithRetry(TABLES.TASK, {
                taskId: task.id,
                name: task.name,
                category: task.category,
                amount: task.amount,
                unit: task.unit || 'minutes',
                type: task.type,
                multiplier: task.multiplier || 1,
                isHabit: task.isHabit || false,
                habitDetails: task.habitDetails || null,
                enableFloatingTimer: task.enableFloatingTimer || false,
                lastUsed: task.lastUsed || null,
                isSystem: task.isSystem || false,
                data: task,
                _openid: currentUid
            })));
            showNotification('📋 上传任务...', Math.min(i + BATCH, tasks.length) + '/' + tasks.length, 'info');
            if (i + BATCH < tasks.length) await new Promise(r => setTimeout(r, DELAY));
        }

        // 6. 上传交易记录（受控并发 + 重试）
        showNotification('💰 上传交易记录...', '0/' + transactions.length, 'info');
        for (let i = 0; i < transactions.length; i += BATCH) {
            const slice = transactions.slice(i, i + BATCH);
            await Promise.all(slice.map(tx => writeWithRetry(TABLES.TRANSACTION, {
                txId: tx.id,
                taskId: tx.taskId,
                taskName: tx.taskName,
                category: tx.category || null,
                amount: tx.amount,
                type: tx.type,
                timestamp: tx.timestamp,
                description: tx.description || '',
                isStreakAdvancement: tx.isStreakAdvancement || false,
                isSystem: tx.isSystem || false,
                data: tx,
                _openid: currentUid
            })));
            showNotification('💰 上传交易记录...', Math.min(i + BATCH, transactions.length) + '/' + transactions.length, 'info');
            if (i + BATCH < transactions.length) await new Promise(r => setTimeout(r, DELAY));
        }

        // 7. 上传运行中任务
        const runningArray = Array.from(runningTasks.entries());
        if (runningArray.length > 0) {
            showNotification('⏱️ 上传运行中任务...', '请稍候', 'info');
            for (const [taskId, runningData] of runningArray) {
                await writeWithRetry(TABLES.RUNNING, { taskId, data: runningData, _openid: currentUid });
            }
        }

        // 8. 上传每日统计（受控并发）
        showNotification('📊 上传每日统计...', '请稍候', 'info');
        const dailyEntries = Object.entries(dailyChanges);
        for (let i = 0; i < dailyEntries.length; i += BATCH) {
            const slice = dailyEntries.slice(i, i + BATCH);
            await Promise.all(slice.map(([date, daily]) => writeWithRetry(TABLES.DAILY, {
                date,
                earned: daily.earned || 0,
                spent: daily.spent || 0,
                _openid: currentUid
            })));
            if (i + BATCH < dailyEntries.length) await new Promise(r => setTimeout(r, DELAY));
        }

        // 9. 重建 Watch
        if (DAL && DAL.subscribeAll) await DAL.subscribeAll();

        // 10. 释放写入门禁
        if (typeof releaseCloudSyncWriteLock === 'function') {
            releaseCloudSyncWriteLock();
        }

        // 11. 刷新 UI
        if (typeof updateAllUI === 'function') updateAllUI();

        if (authStatus) { authStatus.innerHTML = originalText; authStatus.className = 'status-online'; }
        showNotification('✅ 同步完成', '本设备数据已覆盖云端，其他设备将自动追平', 'success');

    } catch (e) {
        console.error('[trustThisDevice] 失败:', e);
        // 确保门禁被释放（即使出错也不永久卡住）
        if (typeof releaseCloudSyncWriteLock === 'function') {
            releaseCloudSyncWriteLock();
        }
        if (authStatus) { authStatus.innerHTML = originalText; authStatus.className = 'status-offline'; }
        showNotification('❌ 同步失败', e.message, 'error');
    }
}

